package com.db.grad.javaapi.model;

import javax.annotation.Generated;
import javax.persistence.*;

@Entity
@Table(name="dogs")
public class Dog
{
    @Id
    private int id;
    private String name;
    private int age;

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public long getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
